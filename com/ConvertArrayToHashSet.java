package com;

import java.util.HashSet;

public class ConvertArrayToHashSet {
	
	public static void main(String[] args) {
		
		int [] a= {1,2,3,2,1};
		
		HashSet<Integer> a1=new HashSet<Integer>();
		
		for(int d:a) {
				a1.add(d);
		}
		System.out.println(a1);
		
	}
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	
		
}
